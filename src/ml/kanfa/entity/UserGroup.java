package ml.kanfa.entity;

/**
 * @author Kanfa.
 */

public class UserGroup {

    private int id;

    private String name;

    public UserGroup(){}

    //Getter and setter


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
